# Segmentation
# ---------------------------------------------------------------------------

def _last_active_by_email() -> dict[str, datetime]:
    """MAX(created_at) per email across ALL crm_events -- deliberately NOT
    sessions.last_seen_at, which refreshes on any page load even without a
    meaningful action. crm_events only has rows for actual logged actions, so
    it's a true activity signal for churn-risk purposes."""
    engine = _get_engine()
    if not engine:
        return {}
    try:
        with Session(engine) as session:
            rows = (session.query(CrmEvent.email, func.max(CrmEvent.created_at))
                    .group_by(CrmEvent.email).all())
            return {email: ts for email, ts in rows}
    except Exception:
        return {}


def agency_ready_emails(window_days: int = AGENCY_READY_WINDOW_DAYS) -> set[str]:
    """Accounts using 2+ distinct donor frameworks, OR running 3+ audits,
    within a rolling window -- computed from crm_events directly (not the
    opt-in-only utils.audits.audits table), so it doesn't blindly miss the
    majority of usage that never opts into saving audit history."""
    engine = _get_engine()
    if not engine:
        return set()
    cutoff = datetime.now(timezone.utc) - timedelta(days=window_days)
    try:
        with Session(engine) as session:
            heavy = (session.query(CrmEvent.email)
                     .filter(CrmEvent.event_type == "audit_run", CrmEvent.created_at >= cutoff)
                     .group_by(CrmEvent.email)
                     .having(func.count(CrmEvent.id) >= 3)
                     .all())
            multi = (session.query(CrmEvent.email)
                     .filter(CrmEvent.event_type == "framework_used", CrmEvent.created_at >= cutoff)
                     .group_by(CrmEvent.email)
                     .having(func.count(func.distinct(CrmEvent.metadata_json["donor"])) >= 2)
                     .all())
            return {r[0] for r in heavy} | {r[0] for r in multi}
    except Exception:
        return set()


def build_segments() -> dict[str, list[dict]]:
    """Returns {"Churn-risk": [...], "Trial": [...], "Active-Free": [...],
    "Professional": [...], "Agency": [...]} -- mutually exclusive buckets,
    computed in that priority order (churn-risk first, across any tier).
    Each row also carries an "agency_ready" boolean (cross-cutting, not a
    6th bucket -- a Professional or Active-Free account can independently
    qualify)."""
    from utils.db import list_all_users
    from utils.metering import FREE_CHECKS_LIMIT

    users = list_all_users()
    last_active = _last_active_by_email()
    ready = agency_ready_emails()
    now = datetime.now(timezone.utc)
    segments: dict[str, list[dict]] = {
        "Churn-risk": [], "Trial": [], "Active-Free": [], "Professional": [], "Agency": [],
    }
    for u in users:
        email = (u.get("email") or "").strip().lower()
        if not email:
            continue
        la = last_active.get(email)
        # SQLite (the in-memory test engine) doesn't preserve tzinfo the way
        # real Postgres does, even on a timezone(True) column -- normalize to
        # UTC so the subtraction below never raises on a naive/aware mismatch.
        if la is not None and la.tzinfo is None:
            la = la.replace(tzinfo=timezone.utc)
        row = {
            "email": email,
            "plan": u.get("plan"),
            "free_checks_used": u.get("free_checks_used"),
            "created_at": u.get("created_at"),
            "last_active_at": la,
            "agency_ready": email in ready,
        }
        # No crm_events row at all (la is None) counts as churned, not
        # "unknown" -- covers accounts created before this table existed and
        # accounts that signed up but never took one logged action.
        if la is None or (now - la).days >= CHURN_WINDOW_DAYS:
            segments["Churn-risk"].append(row)
            continue
        plan = (u.get("plan") or "free").lower()
        if plan == "agency":
            segments["Agency"].append(row)
        elif plan == "professional":
            segments["Professional"].append(row)
        elif (u.get("free_checks_used") or 0) < FREE_CHECKS_LIMIT:
            segments["Trial"].append(row)
        else:
            segments["Active-Free"].append(row)
    return segments


# ---------------------------------------------------------------------------
# Data deletion ("erase my history")
# ---------------------------------------------------------------------------

def purge_account_crm_events(email: str) -> int:
    """Deletes every crm_events row for email. Wired into the same "erase my
    history" flow as utils.audits.purge_account_audit_content() -- unlike
    access_log, this table is marketing/growth data, not a permanent
    compliance trail, so it's in scope for deletion on request."""
    if not email:
        return 0
    engine = _get_engine()
    if not engine:
        return 0
    try:
        with Session(engine) as session:
            n = session.query(CrmEvent).filter(CrmEvent.email == email).delete()
            session.commit()
            return n
    except Exception:
        return 0
